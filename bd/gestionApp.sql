CREATE TABLE UTILISATEUR (  
	ID int(4) AUTO_INCREMENT PRIMARY KEY,
	LOGIN VARCHAR(100) NOT NULL,
	PWD VARCHAR(255) NOT NULL,
	ROLE VARCHAR(50),
	EMAIL VARCHAR(255),
	ETAT INT(1)); 
-- 	ETAT = 0 désactivé
-- 	ETAT = 1 activé

CREATE TABLE SPECIALITE (
	CODE_SPC int(2) PRIMARY KEY, 
	LIBELLE_SPC VARCHAR(100) NOT NULL,    
	DOMAINE_FORM VARCHAR(40),
    DUREE_FORM int(2)); 

CREATE TABLE SESS(                    
	ID_SESS int PRIMARY KEY); 
    
CREATE TABLE ETABLISSEMENT(                    
	NOM_ETABLISS VARCHAR(100) PRIMARY KEY); 

CREATE TABLE APPRENTI (  
	MATRICULE int(5) PRIMARY KEY,
	NOM VARCHAR(50) NOT NULL,
	PRENOM VARCHAR(50) NOT NULL,
    DATE_NAISS DATE,
    NOM_ETABLISS VARCHAR(100) NOT NULL,
    CODE_SPC int(2) NOT NULL,
    DUREE_FORM int(3) NOT NULL,
    ID_SESS int(4) NOT NULL,
    CONSTRAINT FK_APP_SPC FOREIGN KEY (CODE_SPC)
    REFERENCES SPECIALITE(CODE_SPC),
    CONSTRAINT FK_APP_NE FOREIGN KEY (NOM_ETABLISS)
    REFERENCES ETABLISSEMENT(NOM_ETABLISS));

CREATE TABLE STRUCTURE (
	CODE_STRUCT int(4) PRIMARY KEY, 
	LIBELLE_STRUCT VARCHAR(10) NOT NULL,
    DESIGN_STRUCT VARCHAR(100) NOT NULL,
    REGION VARCHAR(40),
    TYPE VARCHAR(40)); 
    
CREATE TABLE STRUCT_APP (  
	MATRICULE int(5) NOT NULL,
	CODE_STRUCT int(4) NOT NULL,
    LIBELLE_STRUCT VARCHAR(10) NOT NULL,
    DESIGN_STRUCT VARCHAR(100) NOT NULL,
    CONSTRAINT FK_STRUCT_APP_MAT FOREIGN KEY (MATRICULE)
    REFERENCES APPRENTI(MATRICULE),
    CONSTRAINT FK_STRUCT_APP_COD FOREIGN KEY (CODE_STRUCT)
    REFERENCES STRUCTURE(CODE_STRUCT),
    CONSTRAINT PK_STRUCT_APP PRIMARY KEY (MATRICULE,CODE_STRUCT));
    
    
CREATE TABLE PAIE (                   
	ID int PRIMARY KEY,
	PAIE float NOT NULL,    
	NB_ABS int(2),
    DATE_P DATE NOT NULL,
    MATRICULE int(5) NOT NULL,
    CONSTRAINT FK_PAIE FOREIGN KEY (MATRICULE)
    REFERENCES APPRENTI(MATRICULE)); 
    
    
CREATE TABLE BESOIN_APPRENTISSAGE(                    
	CODE_STRUCT int(4) NOT NULL, 
    CODE_SPC int(2) NOT NULL,
    ID_SESS int NOT NULL,
    NB_APPR int(3) NOT NULL,
    CONSTRAINT FK_BESAPP_SPC FOREIGN KEY (CODE_SPC)
    REFERENCES SPECIALITE(CODE_SPC),
    CONSTRAINT FK_BESAPP_STRUCT FOREIGN KEY (CODE_STRUCT)
    REFERENCES STRUCTURE(CODE_STRUCT),
    CONSTRAINT FK_BESAPP_SESS FOREIGN KEY (ID_SESS)
    REFERENCES SESS(ID_SESS),
    CONSTRAINT PK_BESAPP PRIMARY KEY (CODE_STRUCT,CODE_SPC,ID_SESS));  
    
CREATE TABLE DAP(                    
	CODE_DAP VARCHAR(8) PRIMARY KEY, 
    LIBELLE_DAP VARCHAR(3) NOT NULL,
    DESIGN_DAP VARCHAR(40),    
    DATE_DAP int NOT NULL,
    MATRICULE int(5) NOT NULL,
    CONSTRAINT FK_DAP FOREIGN KEY (MATRICULE)
    REFERENCES APPRENTI(MATRICULE));  
    
CREATE TABLE RAA(                    
	CODE_RAA VARCHAR(7) PRIMARY KEY,
    LIBELLE_RAA VARCHAR(3),
    DESIGN_RAA VARCHAR(40),
    DATE_RAA int,
    MATRICULE int(5) NOT NULL,
    CONSTRAINT FK_RAA FOREIGN KEY (MATRICULE)
    REFERENCES APPRENTI(MATRICULE)); 

	


